    <p class="lead">The license fee is the only cost that arrives on an invoice, so it's the only one that makes it into most business cases. Then the pilot ships, the savings look thinner than the deck promised, and trust in the whole program takes the blame. The math wasn't wrong — it was incomplete. Here is the version we put in front of every client.</p>

    <h2>The costs that don't invoice you</h2>

    <p>Four line items show up in every deployment we've measured, and almost no vendor deck:</p>

    <ul>
        <li>Review time — minutes of human attention per assistant action, in whichever lane it runs</li>
        <li>Escalations — exceptions the assistant touches first and a person finishes anyway, paying twice</li>
        <li>Maintenance — the knowledge base gardening and prompt upkeep that keeps quality from sagging</li>
        <li>Error cleanup — the rare wrong action, multiplied by what wrong costs in your business</li>
    </ul>

    <p>None of these kill a good project. All of them shrink the headline, and a business case that can't survive their inclusion was never real.</p>

    <h2>Count saved minutes, not saved heads</h2>

    <p>The dishonest version of this math divides saved minutes by 480 and announces it "freed up three people." Minutes don't consolidate that neatly — they come back as slack spread across a team, and what you do with slack is a management decision: faster queues, better coverage, the backlog nobody could get to. We forecast <strong>hours returned per month</strong> and let the client decide what those hours become. It reads less dramatic on a slide. It has the advantage of coming true.</p>

    <blockquote>
        <p>If the case only works when the license is the whole cost, the case doesn't work.</p>
    </blockquote>

    <h2>A worked example</h2>

    <p>A recent pilot, real numbers rounded: 3,100 drafts a month, 2.1 minutes of review each, 9% escalated and handled twice, six hours a month of upkeep. Gross time saved: about 640 hours. Net of review, escalations, and maintenance: about 410. That's the number that went to the CFO — 36% smaller than the naive version, and nobody has had to walk it back since.</p>

    <h2>Payback, stated plainly</h2>

    <p>Roll the honest costs together and state one number: months to payback. Under a year is a good project. Under six months is a great one. "Immediate" is a sign someone left review time out of the model — go find it. Our pilots aim to demonstrate the payback curve within the six weeks, so scaling is a decision about arithmetic you've already watched happen, not a bet on a slide.</p>

